namespace Minesweeper.Core
{
    /// <summary>
    /// Represents the type of sounds available
    /// </summary>
    public enum SoundType
    {
        /// <summary>
        /// Sounds which is played at the start of the game
        /// </summary>
        GameStart,

        /// <summary>
        /// sound played when the game results in a lose
        /// </summary>
        GameLose,

        /// <summary>
        /// sound played when the game results in a win
        /// </summary>
        GameWin
    }
}
